// Cálculo da área do Triângulo


public class Exemplo03 {

    public static final double BASE = 5.5;
    

    public static void main(String[] args) {
        
        double altura = 4.0;
        double area = (BASE * altura)/2;
        System.out.println("A area do triângulo é: " + area + " cm²");

    }

}